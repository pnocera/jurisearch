#!/usr/bin/env bun

/**
 * M5 embed-manifest generator (Spike A mechanism). Walks `web/dist` and emits the gitignored
 * `server/embedded-assets.generated.ts`: one `import ‚Ä¶ with { type: "file" }` per file ‚Äî the
 * directive that makes `bun build --compile` bundle the file's BYTES into the executable ‚Äî plus a
 * map from the REQUEST pathname the M3 router receives (`/index.html`,
 * `/assets/<name>-<hash>.{js,css,woff2}`, keyed off Vite's absolute `/assets/...` refs) to the
 * embedded virtual-path string each import resolves to at runtime. The compiled binary reads those
 * paths via `Bun.file(path)` from any cwd ‚Äî no on-disk `web/dist` (Spike A RESULT.md).
 *
 * If `web/dist` is absent (e.g. `typecheck`/`test` before a `build`), it writes an EMPTY, type-valid
 * stub so the generated file is always present for `tsc` and for `import("‚Ä¶")`; the composition root
 * then falls back to `DevAssetSource`. The generated file is derived entirely from `web/dist`, so it
 * is a pure build artifact (see .gitignore) and carries `// @ts-nocheck` because TS types
 * `with { type: "file" }` imports by extension (`.html`‚ÜíHTMLBundle, `.css`/`.js`/`.woff2`‚Üíunknown),
 * not as the runtime path strings they actually are.
 */

import { existsSync, readdirSync, statSync } from "node:fs";
import { relative, resolve } from "node:path";

const dashboardRoot = resolve(import.meta.dir, "..");
const distDir = resolve(dashboardRoot, "web", "dist");
// The generated file lives in server/, so import specifiers are relative to server/.
const outPath = resolve(dashboardRoot, "server", "embedded-assets.generated.ts");

const HEADER = `// @ts-nocheck
// GENERATED by build/gen-embed.ts ‚Äî DO NOT EDIT. Gitignored build artifact.
// Maps router request paths to assets embedded by \`bun build --compile\` via \`with { type: "file" }\`.
`;

/** Every file under `dir`, recursively (absolute paths). */
function walk(dir: string): string[] {
  const out: string[] = [];
  for (const entry of readdirSync(dir)) {
    const full = resolve(dir, entry);
    if (statSync(full).isDirectory()) {
      out.push(...walk(full));
    } else {
      out.push(full);
    }
  }
  return out;
}

function toPosix(p: string): string {
  return p.split("\\").join("/");
}

/** Empty stub: no embedded assets, so the composition root falls back to `DevAssetSource`. */
function renderStub(): string {
  return `${HEADER}
export const EMBEDDED_INDEX: string | null = null;
export const EMBEDDED_ASSETS: Record<string, string> = {};
`;
}

function render(files: readonly string[]): string {
  const sorted = [...files].sort(); // deterministic output ‚Üí stable diffs/builds
  const imports: string[] = [];
  const entries: string[] = [];
  let indexVar: string | null = null;

  sorted.forEach((abs, i) => {
    const rel = toPosix(relative(distDir, abs)); // e.g. "index.html", "assets/index-abc.js"
    const requestPath = `/${rel}`; // the pathname the router will look up
    const specifier = `../web/dist/${rel}`; // relative to server/ (where this file is written)
    const varName = `asset_${i}`;
    imports.push(`import ${varName} from ${JSON.stringify(specifier)} with { type: "file" };`);
    entries.push(`  ${JSON.stringify(requestPath)}: ${varName},`);
    if (rel === "index.html") {
      indexVar = varName;
    }
  });

  return `${HEADER}${imports.join("\n")}

export const EMBEDDED_INDEX: string | null = ${indexVar ?? "null"};
export const EMBEDDED_ASSETS: Record<string, string> = {
${entries.join("\n")}
};
`;
}

const content = existsSync(distDir) ? render(walk(distDir)) : renderStub();
await Bun.write(outPath, content);
const count = content.match(/with \{ type: "file" \}/g)?.length ?? 0;
console.log(
  `gen-embed: ${outPath} -> ${count} embedded asset(s)${count === 0 ? " (stub; no web/dist)" : ""}`,
);
